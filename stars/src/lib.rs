pub fn stars(n: u32) -> String {
    let mut to_return = String::new();
    for _ in 0..2_i32.pow(n) {
        to_return.push('*')
    }
    to_return
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_stars() {
        assert_eq!(stars(0), "*");
        assert_eq!(stars(1), "**");
        assert_eq!(stars(2), "****");
        assert_eq!(stars(3), "********");
        assert_eq!(stars(4), "****************");
        assert_eq!(stars(5), "********************************");
        assert_eq!(
            stars(10),
            "****************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************************"
        );
    }
}
